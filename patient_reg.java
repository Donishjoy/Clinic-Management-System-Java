    private void register1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_register1ActionPerformed
                      if(name1.getText().isEmpty()||age1.getText().isEmpty()||
               place1.getText().isEmpty()||address1.getText().isEmpty()||contact1.getText().isEmpty()||blood1.getText().isEmpty()){
                  notice.setVisible(true);
           
       }else{
        try{
            String sql="INSERT INTO patient(`name`,`age`,`place`,`address`,`phone`,`blood_group`) values(?,?,?,?,?,?)";
            PreparedStatement prest;
            prest=con.prepareStatement(sql);
            prest.setString(1,name1.getText());
            prest.setString(2,age1.getText());
            prest.setString(3,place1.getText());
            prest.setString(4,address1.getText());
            prest.setString(5,contact1.getText());
            prest.setString(6,blood1.getText());
            int count= prest.executeUpdate();
            if(count>0){
                JOptionPane.showMessageDialog(null, "Successfully inserted");
                admin a=new admin();
                a.setVisible(true);
                this.dispose();

            }
        }
        catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
                      }
    }//GEN-LAST:event_register1ActionPerformed

